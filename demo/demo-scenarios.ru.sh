# =============================================================================
# БОНУС: Adaptive Phase Detection
# =============================================================================

function scenario_bonus() {
  print_header "БОНУСНЫЙ СЦЕНАРИЙ: Adaptive Phase Detection"

  print_note "Цель: Показать возобновление воркфлоу с существующей работы"
  print_note "Настройка: Создать отчёт расследования, затем запустить bug_fix_workflow"
  pause

  # Создать отчёт расследования
  print_step "Шаг 1: Создать существующий отчёт расследования"
  cat > investigation-report.md << 'EOF'
# Расследование бага: Null Pointer в login.ts

## Первопричина
Отсутствует проверка на null для объекта user на строке 42

## Условие срабатывания
Объект user становится undefined когда истекает сессия

## Предлагаемое исправление
Добавить проверку на null перед доступом к свойству user.email

## Затронутые файлы
- src/auth/login.ts (строка 42)

## Необходимые тест-кейсы
1. Валидный объект user
2. Null объект user (истёкшая сессия)
3. Undefined объект user (не аутентифицирован)
EOF

  echo "Создан: investigation-report.md"
  cat investigation-report.md
  pause

  # Запустить воркфлоу
  print_step "Шаг 2: Запустить bug_fix_workflow (должен пропустить Фазу 1)"
  echo "Команда: asmo run \"Исправить null pointer в login handler\" --verbose"
  print_note "Следите за:"
  echo "  • PhaseDetector анализирует существующий контекст"
  echo "  • Обнаружение: найден investigation-report.md"
  echo "  • Решение: Присоединиться к воркфлоу на Фазе 2 (developer)"
  echo "  • Фаза 1 (debugger) пропущена — уже выполнена"
  pause

  asmo run "Исправить null pointer в login handler" --verbose
  pause

  # Очистка
  rm -f investigation-report.md

  print_header "БОНУСНЫЙ СЦЕНАРИЙ ЗАВЕРШЁН ✓"
  print_note "Ключевые выводы:"
  echo "  • PhaseDetector использует LLM для анализа существующей работы"
  echo "  • Автоматически пропускает ненужные фазы"
  echo "  • Экономит время и LLM токены"
  echo "  • Полезно для возобновления прерванной работы"
  pause
}

# =============================================================================
# Главное меню
# =============================================================================

function show_menu() {
  clear
  echo -e "${BLUE}╔═══════════════════════════════════════════════════════╗${NC}"
  echo -e "${BLUE}║     ASMO Tech Demo — Интерактивные сценарии          ║${NC}"
  echo -e "${BLUE}╚═══════════════════════════════════════════════════════╝${NC}"
  echo ""
  echo "Выберите сценарий для запуска:"
  echo ""
  echo "  1) Сценарий 1: YOLO Mode (5 мин)"
  echo "  2) Сценарий 2: Context Cascade (10 мин)"
  echo "  3) Сценарий 3: Party Mode (10 мин)"
  echo "  4) Бонус: Adaptive Phase Detection (5 мин)"
  echo ""
  echo "  5) Запустить все сценарии (30 мин)"
  echo ""
  echo "  0) Выход"
  echo ""
  echo -n "Выбор: "
}

# Основной цикл
while true; do
  show_menu
  read choice

  case $choice in
    1)
      scenario_1
      ;;
    2)
      scenario_2
      ;;
    3)
      scenario_3
      ;;
    4)
      scenario_bonus
      ;;
    5)
      scenario_1
      scenario_2
      scenario_3
      scenario_bonus
      echo -e "\n${GREEN}Все сценарии завершены! 🎉${NC}\n"
      break
      ;;
    0)
      echo "Выход из демо. Спасибо!"
      exit 0
      ;;
    *)
      echo -e "${YELLOW}Неверный выбор. Пожалуйста, выберите 0-5.${NC}"
      sleep 2
      ;;
  esac
done
