#!/bin/bash
# ASMO Tech Demo — Сценарии выполнения
# Запустите каждый сценарий для демонстрации возможностей ASMO

set -e

# Цвета для вывода
GREEN='\033[0;32m'
BLUE='\033[0;34m'
YELLOW='\033[1;33m'
NC='\033[0m' # No Color

function print_header() {
  echo -e "\n${BLUE}========================================${NC}"
  echo -e "${BLUE}$1${NC}"
  echo -e "${BLUE}========================================${NC}\n"
}

function print_step() {
  echo -e "${GREEN}► $1${NC}"
}

function print_note() {
  echo -e "${YELLOW}ℹ  $1${NC}"
}

function pause() {
  echo -e "\n${YELLOW}Нажмите ENTER для продолжения...${NC}"
  read
}

# =============================================================================
# СЦЕНАРИЙ 1: YOLO Mode — Нулевое трение для простых задач
# =============================================================================

function scenario_1() {
  print_header "СЦЕНАРИЙ 1: YOLO Mode — Нулевое трение для простых задач"

  print_note "Цель: Показать, что ASMO не добавляет overhead для простых задач"
  print_note "Ожидаемое: Сложность < 30 → авто-одобрение, выполнение < 60с"
  pause

  # Шаг 1: Получить рекомендацию
  print_step "Шаг 1: Проверить рекомендацию ASMO"
  echo "Команда: asmo suggest \"Исправить опечатку в README\""
  asmo suggest "Исправить опечатку в README"
  pause

  # Шаг 2: Выполнить с замером времени
  print_step "Шаг 2: Выполнить задачу (следите за YOLO bypass)"
  echo "Команда: time asmo run \"Исправить опечатку в README\" --verbose"
  print_note "Следите за: сообщением 'YOLO mode активирован - авто-одобрение'"
  time asmo run "Исправить опечатку в README" --verbose
  pause

  print_header "СЦЕНАРИЙ 1 ЗАВЕРШЁН ✓"
  print_note "Ключевые выводы:"
  echo "  • Оценка сложности < 30 активирует YOLO mode"
  echo "  • Без контрольной точки одобрения — немедленное выполнение"
  echo "  • Выполнение менее чем за 60 секунд"
  echo "  • Устраняет усталость от одобрений для ~20% задач"
  pause
}

# =============================================================================
# СЦЕНАРИЙ 2: Context Cascade — Без потери контекста между фазами
# =============================================================================

function scenario_2() {
  print_header "СЦЕНАРИЙ 2: Context Cascade — Без потери контекста между фазами"

  print_note "Цель: Показать передачу контекста архитектор → разработчик → тестер"
  print_note "Ожидаемое: Каждый агент строит на предыдущей работе, без дублирования"
  pause

  # Шаг 1: Dry run предпросмотр
  print_step "Шаг 1: Предпросмотр плана выполнения (dry run)"
  echo "Команда: asmo run \"Добавить валидацию email в форму регистрации\" --dry-run"
  asmo run "Добавить валидацию email в форму регистрации" --dry-run
  pause

  # Шаг 2: Выполнить с verbose
  print_step "Шаг 2: Выполнить с подробным логированием"
  echo "Команда: asmo run \"Добавить валидацию email в форму регистрации\" --verbose"
  print_note "Следите за передачей контекста между фазами:"
  echo "  • Фаза 1 (Архитектор): Создаёт стратегию валидации"
  echo "  • Фаза 2 (Разработчик): Получает стратегию, реализует"
  echo "  • Фаза 3 (Тестер): Получает стратегию + реализацию, тестирует оба"
  pause

  asmo run "Добавить валидацию email в форму регистрации" --verbose
  pause

  print_header "СЦЕНАРИЙ 2 ЗАВЕРШЁН ✓"
  print_note "Ключевые выводы:"
  echo "  • ContextCascade автоматически передаёт Фаза N → Фаза N+1"
  echo "  • Нет потери контекста между агентами"
  echo "  • Каждый агент строит на предыдущей работе (без повторений)"
  echo "  • Фаза 3 видит полную историю: решения + реализация"
  pause
}

